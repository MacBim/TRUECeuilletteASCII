
public class Position {
	
	int X; // X coordinate
	int Y; // Y coordinate
	
	public Position(int x, int y) {
		X = x;
		Y = y;
	}
	
	public int getX() {
		return X;
	}
	
	public void setX(int x) {
		X = x;
	}
	
	public int getY() {
		return Y;
	}
	
	public void setY(int y) {
		Y = y;
	}
}
